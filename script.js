function NSI(){
    var element = document.getElementsByClassName("NSI");
    for(let i=0; i < element.length; i++){
        if(element[i].bgColor==="#d6d6d6"){
            element[i].bgColor="#b8c8d1";
        }else{element[i].bgColor="#d6d6d6"}
    }
}
